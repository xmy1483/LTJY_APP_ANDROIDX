public class FlowBean {

    private Object product_id;
    private Object product_name;
    private Object market_price;
    private Object donate_money;
    private Object price;


    public Object getProduct_id() {
        return product_id;
    }

    public void setProduct_id(Object product_id) {
        this.product_id = product_id;
    }

    public Object getProduct_name() {
        return product_name;
    }

    public void setProduct_name(Object product_name) {
        this.product_name = product_name;
    }

    public Object getMarket_price() {
        return market_price;
    }

    public void setMarket_price(Object market_price) {
        this.market_price = market_price;
    }

    public Object getDonate_money() {
        return donate_money;
    }

    public void setDonate_money(Object donate_money) {
        this.donate_money = donate_money;
    }

    public Object getPrice() {
        return price;
    }

    public void setPrice(Object price) {
        this.price = price;
    }

}
